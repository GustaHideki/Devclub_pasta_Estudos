/*
Functions / Função 

Função void (vazia), não retorna nada.

Função return, retorna um valor para quem chamou a função



*/

//função void(vazia)
function sum(value, value2){
    console.log(value + value2)
}

const resultt = sum(10,20)
console.log(resultt + " resultado")

/////////////////////////////////////////////////////////////////////////////////////////////////////////

// função return
function sub(value, value2){
    const result = value + value2
    return result
}



const result = sub(10,20)
console.log(result + " resultado")

